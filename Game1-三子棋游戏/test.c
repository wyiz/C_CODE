#define _CRT_SECURE_NO_WARNINGS 1
#pragma warning(disable:6031)


#include "game.h"

void menu()
{
	printf("*****************************\n");
	printf("******     1.play     *******\n");
	printf("******     0.exit      ******\n");
	printf("*****************************\n");

}

void game()
{
	//需要创建一个二维数组来存储数据
	char board[ROW][COL] = { 0 };

	//初始化棋盘 --初始化为空格
	InitBoard(board,ROW,COL);

	//打印棋盘
	Display(board,ROW,COL);

	//玩家走

	//电脑走

}

int main()
{
	int input = 0;
	do
	{
		menu();
		printf("请选择>:");
		scanf("%d",&input);

		switch (input)
		{
		case 1:
			game();
			
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("输入错误，请重新选择\n");
			break;
		}

	} while (input);

	return 0;
}